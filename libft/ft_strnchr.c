#include "libft.h"

char	*ft_strnchr(const char *s, int c, int n)
{
	int i;

	i = 0;
	while (s[i] != '\0' && s[i] != c && i <= n)
		i++;
	if (s[i] == c)
		return ((char *)s + i);
	return (NULL);
}
